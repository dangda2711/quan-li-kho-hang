    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
         List<hanghoa> dshanghoa = HangHoaModify.findAll();
        int selectedIndex = tblKho.getSelectedRow();
        hanghoa hang = dshanghoa.get(selectedIndex);
         
               hang.setTenhang(txtTenhang.getText()) ;
               hang.setLoaihang(ComboLoaihang.getSelectedItem().toString());
               hang.setSoluonghang(Integer.parseInt(txtSoluong.getText()));
               hang.setMotahang(textMotahang.getText());
               HangHoaModify.update(hang);
               showKho();
               showThongKe();
            
        
        
    }//GEN-LAST:event_btnEditActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tenhang = txtTenhang.getText();
        String loaihang = ComboLoaihang.getSelectedItem().toString();
        int soluonghang = Integer.parseInt(txtSoluong.getText());
        String motahang = textMotahang.getText();
        
        
        hanghoa hang  = new hanghoa(tenhang, loaihang, soluonghang,motahang);
        
        HangHoaModify.insert(hang);
        
        showKho();
        showThongKe();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        List<hanghoa> dshanghoa = HangHoaModify.findAll();
        int selectedIndex = tblKho.getSelectedRow();
        if(selectedIndex >= 0) {
            hanghoa hang = dshanghoa.get(selectedIndex);
            
            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this item?");
            System.out.println("option : " + option);
            
            if(option == 0) {
               HangHoaModify.delete(hang.getId());
                
                showKho();
                showThongKe();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        List<hanghoa> dshanghoa = new ArrayList<>();
        String input = JOptionPane.showInputDialog(this, "Enter full name to search");
        if(input != null && input.length() > 0) {
            dshanghoa = HangHoaModify.findByFullname(input);
            
            tableModel.setRowCount(0);
        
            dshanghoa.forEach((hang) -> {
                tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, hang.getTenhang(), 
                   hang.getLoaihang(), hang.getSoluonghang(), hang.getMotahang()});
            });
        } else {
            showKho();
            showThongKe();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

