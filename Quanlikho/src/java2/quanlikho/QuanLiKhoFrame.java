    private void showKho() {
        List<hanghoa> dshanghoa = HangHoaModify.findAll();
        
        tableModel.setRowCount(0);
        dshanghoa.forEach((hanghoa) -> {
            tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, hanghoa.getTenhang(), 
                hanghoa.getLoaihang(), hanghoa.getSoluonghang(), hanghoa.getMotahang()});
        });
    }
    private void showThongKe() {
        List<Loaihang> dsHanghoa= HangHoaModify.thongKe();
        tableModel2.setRowCount(0);
        dsHanghoa.forEach((Loaihang)-> {
           
                tableModel2.addRow(new Object[] { tableModel2.getRowCount() + 1,Loaihang.getLoaihang(), Loaihang.getSoluongloai(), Loaihang.getTonghang()});
            
        });
        
    }
